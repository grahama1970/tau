    "proof_scope": {
        "proves": [
            "Tau ran a live OMP command identity probe through omp-worker-doctor.",
            "Tau invoked the configured OMP-compatible RPC command through omp-worker-launch.",
            "Tau validated a bounded OMP worker result artifact with omp-worker-validate.",
        ],
        "does_not_prove": [
            "OMP performed live coding work.",
            "The worker is trustworthy.",
            "The code is semantically correct.",
            "Provider/model semantic quality.",
            "Full sandbox isolation.",
        ],
    },
}
demo_path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n", encoding="utf-8")
if not ok:
    raise SystemExit(1)
