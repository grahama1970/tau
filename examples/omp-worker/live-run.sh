#!/usr/bin/env bash
set -euo pipefail

OUT_DIR="${1:-.tmp/tau-omp-worker-live-example}"
if [[ -z "${OMP_BIN:-}" ]]; then
  echo "OMP_BIN must point to an installed oh-my-pi/omp-compatible binary for live OMP sanity" >&2
  exit 2
fi

WORKSPACE_DIR="${OUT_DIR}/workspace"
REPO_DIR="${WORKSPACE_DIR}/repo"
RECEIPTS_DIR="${REPO_DIR}/.tau/receipts"
WORK_ORDER="${WORKSPACE_DIR}/work-order.json"
RESULT="${RECEIPTS_DIR}/omp-result.json"
RECEIPT="${RECEIPTS_DIR}/omp-worker-receipt.json"
LAUNCH_RECEIPT="${OUT_DIR}/omp-worker-launch-live-receipt.json"
DOCTOR_RECEIPT="${OUT_DIR}/omp-worker-doctor-live-receipt.json"
DEMO_RECEIPT="${OUT_DIR}/demo-receipt.json"
SANDBOX_RECEIPT="${RECEIPTS_DIR}/sandbox-run-receipt.json"

mkdir -p "${RECEIPTS_DIR}"
printf '# Tau live OMP worker schema probe\n\nDo not edit this file.\n' > "${REPO_DIR}/README.md"

python3 - "${WORK_ORDER}" "${SANDBOX_RECEIPT}" "${RESULT}" "${REPO_DIR}" <<'PY'
