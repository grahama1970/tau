    "schema": "tau.omp_worker_result.v1",
    "status": "NEEDS_REVIEW",
    "goal_hash": goal_hash,
    "changed_files": [],
    "artifacts": [],
    "tests_run": [],
    "findings": [],
    "next_recommended_route": "reviewer",
}
result_path.write_text(json.dumps(result, indent=2, sort_keys=True) + "\n", encoding="utf-8")
PY

write_blocked_demo() {
  local stage="$1"
  local exit_code="$2"
  python3 - "${DEMO_RECEIPT}" "${RECEIPT}" "${LAUNCH_RECEIPT}" "${DOCTOR_RECEIPT}" "${OMP_BIN}" "${stage}" "${exit_code}" <<'PY'
