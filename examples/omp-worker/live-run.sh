import sys
from pathlib import Path

demo_path = Path(sys.argv[1])
receipt_path = Path(sys.argv[2])
launch_receipt_path = Path(sys.argv[3])
doctor_receipt_path = Path(sys.argv[4])
omp_bin = sys.argv[5]
stage = sys.argv[6]
exit_code = int(sys.argv[7])

def read_json(path: Path) -> dict:
    if not path.exists():
        return {}
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception as exc:
        return {"read_error": str(exc)}

receipt = read_json(receipt_path)
launch_receipt = read_json(launch_receipt_path)
doctor_receipt = read_json(doctor_receipt_path)
payload = {
    "schema": "tau.omp_worker_live_example_receipt.v1",
    "ok": False,
    "status": "BLOCKED",
    "mocked": False,
    "live": "mixed",
    "provider_live": False,
    "blocked_stage": stage,
    "blocked_exit_code": exit_code,
    "omp_bin": omp_bin,
    "omp_command_source": "env",
    "worker_result_source": "tau_probe_fixture",
    "worker_receipt_path": str(receipt_path),
    "worker_receipt_schema": receipt.get("schema"),
    "worker_receipt_status": receipt.get("status"),
    "worker_receipt_alert_codes": receipt.get("alert_codes", []),
    "doctor_receipt_path": str(doctor_receipt_path),
    "doctor_receipt_schema": doctor_receipt.get("schema"),
    "doctor_receipt_status": doctor_receipt.get("status"),
    "doctor_command_found": doctor_receipt.get("command_found"),
    "doctor_version_executed": doctor_receipt.get("version_executed"),
    "doctor_version_exit_code": doctor_receipt.get("version_exit_code"),
    "doctor_alert_codes": doctor_receipt.get("alert_codes", []),
    "doctor_command_path": doctor_receipt.get("command_path"),
    "launch_receipt_path": str(launch_receipt_path),
    "launch_receipt_schema": launch_receipt.get("schema"),
    "launch_receipt_status": launch_receipt.get("status"),
    "launch_receipt_alert_codes": launch_receipt.get("alert_codes", []),
    "apply_launch_process_executed": launch_receipt.get("process_executed"),
    "apply_launch_exit_code": launch_receipt.get("exit_code"),
    "apply_launch_stdout_path": launch_receipt.get("stdout_path"),
    "apply_launch_stdout_sha256": launch_receipt.get("stdout_sha256"),
    "apply_launch_stderr_path": launch_receipt.get("stderr_path"),
    "apply_launch_stderr_sha256": launch_receipt.get("stderr_sha256"),
    "apply_launch_stdout_jsonl_valid": launch_receipt.get("stdout_jsonl_valid"),
    "apply_launch_response_frame_count": launch_receipt.get("response_frame_count"),
    "apply_launch_response_schemas": launch_receipt.get("response_schemas", []),
    "apply_launch_log_artifacts": launch_receipt.get("log_artifacts", []),
    "proof_scope": {
        "proves": [
            "Tau attempted a live OMP-compatible worker probe.",
            "Tau recorded the stage that blocked before claiming live OMP worker readiness.",
        ],
        "does_not_prove": [
            "OMP performed live coding work.",
            "The worker is trustworthy.",
            "The code is semantically correct.",
            "Provider/model semantic quality.",
            "Full sandbox isolation.",
        ],
    },
}
demo_path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n", encoding="utf-8")
PY
}

set +e
uv run tau omp-worker-doctor \
  --out "${DOCTOR_RECEIPT}" \
  --omp-bin "${OMP_BIN}" \
  --timeout-s "${TAU_OMP_LIVE_TIMEOUT_S:-10}" \
  >"${OUT_DIR}/omp-worker-doctor-live.stdout.json"
doctor_exit=$?
set -e
if [[ "${doctor_exit}" -ne 0 ]]; then
  write_blocked_demo "omp-worker-doctor" "${doctor_exit}"
  cat "${DEMO_RECEIPT}"
  exit "${doctor_exit}"
fi

set +e
uv run tau omp-worker-launch \
  --work-order "${WORK_ORDER}" \
  --out "${LAUNCH_RECEIPT}" \
  --apply \
  --omp-bin "${OMP_BIN}" \
  --timeout-s "${TAU_OMP_LIVE_TIMEOUT_S:-60}" \
  >"${OUT_DIR}/omp-worker-launch-live.stdout.json"
launch_exit=$?
set -e
if [[ "${launch_exit}" -ne 0 ]]; then
  write_blocked_demo "omp-worker-launch" "${launch_exit}"
  cat "${DEMO_RECEIPT}"
  exit "${launch_exit}"
fi

set +e
uv run tau omp-worker-validate \
  --work-order "${WORK_ORDER}" \
  --result "${RESULT}" \
  --out "${RECEIPT}" \
  >"${OUT_DIR}/omp-worker-validate-live.stdout.json"
validate_exit=$?
set -e
if [[ "${validate_exit}" -ne 0 ]]; then
  write_blocked_demo "omp-worker-validate" "${validate_exit}"
  cat "${DEMO_RECEIPT}"
  exit "${validate_exit}"
fi

python3 - "${DEMO_RECEIPT}" "${RECEIPT}" "${LAUNCH_RECEIPT}" "${DOCTOR_RECEIPT}" "${OMP_BIN}" <<'PY'
