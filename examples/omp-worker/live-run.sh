    "schema": "tau.data_boundary.v1",
    "classification": "public",
    "export_controlled": False,
    "itar": False,
    "technical_data": False,
    "foreign_person_access": "allowed",
    "external_provider_allowed": False,
    "external_research_allowed": False,
    "public_repo_allowed": False,
    "notes": ["live OMP RPC probe with public temp data only"],
}
work_order = {
    "schema": "tau.executor.omp.v1",
    "dag_id": "live-omp-worker-schema-probe",
    "node_id": "coder",
    "agent": "coder",
    "goal_hash": goal_hash,
    "attempt": 1,
    "repo": str(repo_dir),
    "allowed_paths": [".tau/receipts/**"],
    "forbidden_paths": ["secrets/**", "src/**", "tests/**", "README.md"],
    "task": (
        "Return a bounded response only. Do not edit files. Tau will validate a "
        "separate prewritten worker result artifact for this live OMP transport probe."
    ),
    "required_artifacts": [],
    "result_path": ".tau/receipts/omp-result.json",
    "receipt_path": ".tau/receipts/omp-worker-receipt.json",
